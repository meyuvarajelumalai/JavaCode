package com.yuva;

public class Switch {
	public static void main(String[] args) {
		int i = 3;
		switch (i) {
		case 1:
			System.out.println("20");
			break;
		case 2:
			System.out.println("40");
			break;
		case 3:
			System.out.println("60");
			break;
		default:
			System.out.println("invalid number");
			break;
		case 20:
			System.out.println("20");
			break;
		}
	}
}
